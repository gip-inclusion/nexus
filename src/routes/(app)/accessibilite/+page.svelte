    <h2>Déclaration d'accessibilité</h2>
</header>
<hr class="border-t border-gray-200" />
<div class="p-8">
    <p><strong>Le GIP Plateforme de l’inclusion</strong> s’engage à rendre ses sites internet et applications accessibles conformément à l’article 47 de la loi n°2005-102 du 11 février 2005. </p>
    <p>Son schéma pluriannuel décrit les points importants sur lesquels le GIP Plateforme de l’inclusion s’appuiera pour améliorer l’accessibilité numérique de l’ensemble de ses sites internet et applications. Ce schéma s’accompagne du plans d’action annuels (2023) qui détaillent les opérations programmées et mises en œuvre pour l'année courante, ainsi que l’état de suivis de ces actions, détaillé dans le schéma pluriannuel d’accessibilité 2023-2026.</p>
    <p>Cette déclaration d'accessibilité s'applique au site web <strong>Nexus</strong></p>
</div>
